const DICTS: Record<Lang, Dict> = { zh, en, my };

type Ctx = { lang: Lang; t: (key: string) => string; setLang: (l: Lang) => void };
const I18nContext = createContext<Ctx>({ lang: 'zh', t: (k) => k, setLang: () => {} });

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Lang>('zh');
  useEffect(() => {
    try {
      const saved = localStorage.getItem('__ML_LANG__') as Lang | null;
      if (saved && DICTS[saved]) setLangState(saved);
    } catch {}
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem('__ML_LANG__', l); } catch {}
  };
  const t = (key: string) => DICTS[lang][key] || DICTS.zh[key] || '';
  const value = useMemo(() => ({ lang, t, setLang }), [lang]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export function useI18n() {
  return useContext(I18nContext);
}


